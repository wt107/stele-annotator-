# 简繁转换（内置备用，不依赖 opencc）
# ───────────────────────────────────────────────
def to_traditional(text):
    """简体转繁体（内置映射表）"""
    if not text:
        return text
    result = []
    for char in text:
        if char in VARIANT_MAP:
            trad = VARIANT_MAP[char]
            result.append(trad[0] if isinstance(trad, list) else trad)
        else:
            result.append(char)
    return "".join(result)


def to_simplified(text):
    """繁体转简体（内置映射表）"""
    if not text:
        return text
    result = []
    for char in text:
        result.append(TRAD_TO_SIMP.get(char, char))
    return "".join(result)


def normalize_char(char, target="simplified"):
    """统一字符到目标字形"""
    if not char or char in PUNCTUATION or char.isspace():
        return char
    if target == "simplified":
        return to_simplified(char) if char in TRAD_TO_SIMP else char
    else:
        return to_traditional(char) if char in VARIANT_MAP else char


def _is_cjk_char(char):
    """检测字符是否为 CJK 汉字（包括扩展区）

    支持的 Unicode 范围：
    - 基本汉字：U+4E00-U+9FFF (20,992个)
    - 扩展A区：U+3400-U+4DBF (6,592个)
    - 扩展B区：U+20000-U+2A6DF (42,720个)
    - 扩展C区：U+2A700-U+2B73F (4,160个)
    - 扩展D区：U+2B740-U+2B81F (224个)
    - 扩展E区：U+2B820-U+2CEAF (5,776个)
    - 扩展F区：U+2CEB0-U+2EBEF (7,488个)
    - 扩展G区：U+30000-U+3134F (4,944个)
    """
    code = ord(char)
    # 基本汉字和扩展A区
    if 0x4E00 <= code <= 0x9FFF:
        return True
    if 0x3400 <= code <= 0x4DBF:
        return True
    # 扩展B-G区（需要使用高码点）
    if 0x20000 <= code <= 0x2A6DF:  # 扩展B区
        return True
    if 0x2A700 <= code <= 0x2B73F:  # 扩展C区
        return True
    if 0x2B740 <= code <= 0x2B81F:  # 扩展D区
        return True
    if 0x2B820 <= code <= 0x2CEAF:  # 扩展E区
        return True
    if 0x2CEB0 <= code <= 0x2EBEF:  # 扩展F区
        return True
    if 0x30000 <= code <= 0x3134F:  # 扩展G区
        return True
    return False
